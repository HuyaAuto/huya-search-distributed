public class IgnorableConvertException extends Exception {

    public IgnorableConvertException() {
        super();
    }

    public IgnorableConvertException(String message) {
        super(message);
    }

    public IgnorableConvertException(Throwable cause) {
        super(cause);
    }

    public IgnorableConvertException(String message, Throwable cause) {
        super(message, cause);
    }
}
